import 'package:flutter/material.dart';
import 'package:flutter_web3/flutter_web3.dart';
import 'package:injectable/injectable.dart';

@injectable
class MetaMaskProvider extends ChangeNotifier {
  static const operatingChain = 4;
  String currentAddress = '';
  int currentChain = -1;
  bool get isEnabled => ethereum != null;
  bool get isInOperatingChain => currentChain == operatingChain;
  bool get isConnected => isEnabled && currentAddress.isNotEmpty;

  static const _deployedContractAddress =
      "0x7144476083c8dAf668e4163f063cCa86a87E387B";

  Future<void> connect() async {
    if (isEnabled) {
      final accounts = await ethereum!.requestAccount();
      if (accounts.isNotEmpty) {
        currentAddress = accounts.first;
      }

      currentChain = await ethereum!.getChainId();

      notifyListeners();
    }
  }

  Future<void> buy() async {
    const totalAmount = 2000;
    const abiERC20 =
        "function approve(address _spender, uint256 _value) public returns (bool success)";
    const wethAddress = "0xc778417E063141139Fce010982780140Aa0cD5Ab";

    final abiERC20contract = Contract(wethAddress, abiERC20, provider!);
    await abiERC20contract.call<dynamic>('approve', [
      _deployedContractAddress,
      totalAmount * 1.02,
    ]);

    final contract = Contract(_deployedContractAddress, abi, provider!);
    await contract.call<void>('buy', [totalAmount]);
  }

  Future<void> sell() async {
    const totalAmount = 2000;
    final contract = Contract(_deployedContractAddress, abi, provider!);

    await contract.call('sell', [totalAmount]);
  }

  clear() {
    currentAddress = '';
    currentChain = -1;
  }

  init() {
    if (isEnabled) {
      ethereum!.onAccountsChanged((accounts) {
        clear();
      });
      ethereum!.onChainChanged((chainId) {
        clear();
      });
    }
  }
}

